
public class Ball 
{
	private static int ballX = 500;
	private static int ballY = 500;
	
	public static int getBallX()
	{
		return ballX;
	}
	
	public static void setBallX(int x)
	{
		ballX = x;
	}
	
	
	public static int getBallY()
	{
		return ballY;
	}
	
	public static void setBallY(int y)
	{
		ballY = y;
	}
}
